package com.jio.prb.utils;

public class LastSeenConstants {

    public static String DATE_FORMAT = "dd-MM-yyyy'T'HH:mm:ss";
    public static final int SECOND = 1000;
    public static final int MINUTE = 60 * SECOND;
    public static final int HOUR = 60 * MINUTE;
    public static final double DAY =  24 * HOUR;
    public static final double WEEK = 7 * DAY;
    public static final double MONTH = DAY * 30;
    public static final double YEAR = WEEK * 52;
}
